//Incremento y decremento.
let puntaje = 5;

//Incrementar
puntaje++ //5 | Se manda a llamar y se incrementa.
++puntaje //6 | Se incrementa y se llama.

//Decremento
puntaje-- //5 | Se manda a llamar y se decrementa.
--puntaje //4 | Se decrementa y se manda a llamar.

//Incremento +=/-=
puntaje += 3; //8 | Tomá los 5 de puntaje e incrementale 3. 
puntaje -= 3; //2 | Tomá los 5 de puntaje y restale los 3.
